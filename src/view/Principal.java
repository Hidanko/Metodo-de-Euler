    private void botaoCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoCalcularActionPerformed
        X = Double.parseDouble(textX.getText());
        Y = Double.parseDouble(textY.getText());
        H = Double.parseDouble(textH.getText());
        XN = Double.parseDouble(textXN.getText());

        while (X <= XN) {
            K = H * FUNCAO(X, Y);
            Y = Y + K;
            X = X + H;

            System.out.println("X:" + X);
            System.out.println("Y: " + Y);

            // ADICIONAR VALORES PARA A TABELA AQUI (X e Y)
        }
    }//GEN-LAST:event_botaoCalcularActionPerformed

    private void botaoCalcularAprimoradoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoCalcularAprimoradoActionPerformed
        X = Double.parseDouble(textX.getText());
        Y = Double.parseDouble(textY.getText());
        H = Double.parseDouble(textH.getText());
        XN = Double.parseDouble(textXN.getText());
        double K1, K2;
        while (X <= XN) {
            K1 = FUNCAO(X, Y);
            K2 = FUNCAO(X + H, Y * K1);
            Y = Y + H / 2.0 * (K1 + K2);
            X = X + H;

            System.out.println("X:" + X);
            System.out.println("Y: " + Y);

            // ADICIONAR VALORES PARA A TABELA AQUI (X e Y)
        }
    }//GEN-LAST:event_botaoCalcularAprimoradoActionPerformed

